// ----------------------------------------
// PACKAGE IMPORTS
// ----------------------------------------
import React, {Component} from 'react';
import {View} from 'react-native';

// ----------------------------------------
// LOCAL & CONFIG IMPORTS
// ----------------------------------------
import {Colors, Sizes} from '../../../constants';
import Styles from './style';

// ----------------------------------------
// COMPONENT IMPORTS
// ----------------------------------------
import {TextS} from '../../derivatives/Text';
import {Padder as PadderContainer} from '../../containers';

/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  MAIN CLASS
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */
export default class SimpleList extends Component {
  // ----------------------------------------
  // ----------------------------------------
  // RENDERS
  // ----------------------------------------

  _renderTitle() {
    if (!this.props.title) {
      return null;
    }

    return <TextS color={Colors.main.baseGray}>{this.props.title}</TextS>;
  }

  // ----------------------------------------
  // ----------------------------------------
  // MAIN RENDER
  // ----------------------------------------

  render() {
    return (
      <View style={Styles.container}>
        <View
          style={[
            Styles.title.container,
            !this.props.title ? {paddingVertical: 4} : {},
          ]}>
          <PadderContainer>{this._renderTitle()}</PadderContainer>
        </View>

        {this.props.children}
      </View>
    );
  }

  // ----------------------------------------
}
